package class07;

import class07.Node;

public class Edge {
    int weight;
    Node from;
    Node to;

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public Node getForm() {
        return from;
    }

    public void setForm(Node form) {
        this.from = form;
    }

    public Node getTo() {
        return to;
    }

    public void setTo(Node to) {
        this.to = to;
    }
}
